# 4. Nu evolution timeline
# =========================================================================
def render_nu_timeline():
    fig, ax = plt.subplots(figsize=(10.5, 5.0), dpi=120)
    ax.set_facecolor(DARK_BG)

    # Stages
    stages = [
        ("#1\npre-EX-1-007\n40-cell\nzeroGradient T", 3.0, FAIL),
        ("#2\n80-cell\nfixedValue T", 5.85, FAIL),
        ("#3\nc3a1a4c\nnOuter=80\ntighter URFs", 5.85, FAIL),   # same Nu; marker only
        ("#4\nEX-1-007 B1\n256² graded=6\nlocal mid-height", 66.25, WARN),
        ("#5\nEX-1-008\n256² graded\nmean-over-y", 77.82, FUSE),
    ]
    xs = np.arange(len(stages))
    ys = [s[1] for s in stages]
    colors = [s[2] for s in stages]

    # Tolerance band around gold=30
    gold = 30.0
    tol = 0.15
    ax.axhspan(gold*(1-tol), gold*(1+tol), color=OK, alpha=0.12,
               label=f"gold tolerance band (30 ± 15%)")
    ax.axhline(gold, color=OK, linewidth=1.5, linestyle="-",
               label=f"gold ref_value = {gold}")
    ax.axhspan(100, 160, color=FUSE, alpha=0.10,
               label="literature Ra=1e10 range (100-160)")

    # Trajectory line
    ax.plot(xs, ys, color=FG_MUTE, linewidth=1.3, linestyle="-", alpha=0.6, zorder=2)
    for x, y, c in zip(xs, ys, colors):
        ax.scatter([x], [y], s=240, color=c, edgecolor=FG, linewidth=1.5, zorder=4)
        ax.text(x, y+5, f"{y:.2f}", ha="center", fontsize=9.5, color=c,
                fontweight="bold")

    # FUSE annotation
    ax.annotate("FUSE\n(cycle 2 aborted)", xy=(4, 77.82), xytext=(4.3, 125),
                fontsize=10, color=FUSE, fontweight="bold",
                ha="left", va="center",
                arrowprops=dict(arrowstyle="->", color=FUSE, lw=1.5))

    ax.set_xticks(xs)
    ax.set_xticklabels([s[0] for s in stages], fontsize=8.5)
    ax.set_ylim(-5, 180)
    ax.set_ylabel("Nu_mean at Ra=10¹⁰", fontsize=10)
    ax.set_title("DHC Nusselt-number evolution across 5 measurement stages\n"
                 "sources: DEC-ADWM-004 FUSE record + ex1_007 measurement_result.yaml",
                 fontsize=11)
    ax.grid(axis="y", alpha=0.2, linestyle="--", color=FG_MUTE)
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)
    ax.legend(loc="upper left", fontsize=8.5, framealpha=0.9)

    fig.tight_layout()
    fig.savefig(HERE / "04_nu_timeline.svg", bbox_inches="tight")
    plt.close(fig)
    print("OK 04_nu_timeline.svg")


