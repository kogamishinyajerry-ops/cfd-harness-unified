# 5. Nu ~ Ra scaling with literature cross-check
# =========================================================================
def render_scaling():
    fig, ax = plt.subplots(figsize=(9.5, 5.5), dpi=120)
    ax.set_facecolor(DARK_BG)

    # Literature points
    lit = [
        # (Ra, Nu, label, color, marker)
        (1.58e9, 62, "Ampofo & Karayiannis 2003\n2D air", ACCENT, "o"),
        (1e10, 140, "Fusegi 1991 (3D DHC)\nRa=1e10", ACCENT, "s"),
        (1e10, 30, "Dhir 2001\n(current gold ref)", WARN, "D"),
    ]
    our = [
        (1e10, 5.85,  "run #2 · 80-cell",      FAIL, "x"),
        (1e10, 66.25, "run #4 · 256² local",   WARN, "x"),
        (1e10, 77.82, "run #5 · 256² mean-y",  FUSE, "*"),
    ]

    # Nu ~ Ra^0.28 extrapolation anchored at Ampofo point
    Ra_line = np.logspace(6, 11, 200)
    Ra0, Nu0 = 1.58e9, 62
    Nu_line = Nu0 * (Ra_line / Ra0)**0.28
    ax.plot(Ra_line, Nu_line, color=ACCENT, linestyle=":", linewidth=1.5,
            label=r"Nu ∝ Ra$^{0.28}$ extrapolation (anchored at Ampofo)")

    # Literature band at Ra=1e10
    ax.axhspan(100, 160, xmin=0.78, xmax=0.92, color=FUSE, alpha=0.15,
               label="literature Ra=1e10 range")

    # Literature points
    for Ra, Nu, lbl, c, m in lit:
        ax.scatter([Ra], [Nu], s=150, color=c, marker=m, edgecolor=FG,
                   linewidth=1, zorder=4)
        ax.annotate(lbl, xy=(Ra, Nu), xytext=(8, 8), textcoords="offset points",
                    fontsize=8, color=c, ha="left")

    # Our points
    for Ra, Nu, lbl, c, m in our:
        ax.scatter([Ra], [Nu], s=200, color=c, marker=m, edgecolor=FG,
                   linewidth=1.5, zorder=5)

    # Annotate our trajectory separately (below axis)
    legend_elements = [
        Line2D([0], [0], marker="x", color="w", markerfacecolor=FAIL, markeredgecolor=FAIL,
               markersize=10, label="our #2 (Nu=5.85)", linestyle=""),
        Line2D([0], [0], marker="x", color="w", markerfacecolor=WARN, markeredgecolor=WARN,
               markersize=10, label="our #4 (Nu=66.25)", linestyle=""),
        Line2D([0], [0], marker="*", color="w", markerfacecolor=FUSE, markeredgecolor=FG,
               markersize=14, label="our #5 (Nu=77.82)", linestyle=""),
        Line2D([0], [0], color=ACCENT, linestyle=":", linewidth=1.5,
               label=r"Nu ∝ Ra$^{0.28}$"),
    ]

    ax.set_xscale("log")
    ax.set_yscale("log")
    ax.set_xlim(5e8, 2e11)
    ax.set_ylim(2, 300)
    ax.set_xlabel("Ra (Rayleigh number)", fontsize=10)
    ax.set_ylabel("Nu (Nusselt number)", fontsize=10)
    ax.set_title("Nu–Ra scaling: literature sources + our 5 measurements\n"
                 "The 77.82 measurement lands in the theoretically expected quadrant;\n"
                 "the gold ref_value=30 sits one decade below literature at Ra=1e10",
                 fontsize=10.5)
    ax.grid(True, which="both", alpha=0.2, linestyle="--", color=FG_MUTE)
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)
    first_legend = ax.legend(loc="upper left", fontsize=8.5, framealpha=0.9)
    ax.add_artist(first_legend)
    ax.legend(handles=legend_elements, loc="lower right", fontsize=8.5,
              framealpha=0.9, title="Our measurements", title_fontsize=8.5)

    fig.tight_layout()
    fig.savefig(HERE / "05_nu_ra_scaling.svg", bbox_inches="tight")
    plt.close(fig)
    print("OK 05_nu_ra_scaling.svg")


