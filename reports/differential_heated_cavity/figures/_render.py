# =========================================================================
def render_geometry():
    fig, ax = plt.subplots(figsize=(6.2, 5.6), dpi=120)
    L = 1.0  # aspect ratio 1.0 per adapter

    # Fluid domain
    cavity = mpatches.Rectangle((0, 0), L, L, linewidth=1.5,
                                 edgecolor=FG_MUTE, facecolor="#1e242c", zorder=1)
    ax.add_patch(cavity)

    # Temperature gradient fill (schematic — NOT a solver field)
    grad = np.linspace(0, 1, 200)[None, :]
    ax.imshow(np.tile(grad, (50, 1)), extent=(0, L, 0, L),
              aspect="auto", cmap="coolwarm_r", alpha=0.18, origin="lower", zorder=0)

    # Boundary walls (colored lines)
    wall_w = 3.5
    ax.plot([0, 0], [0, L], color=HOT, linewidth=wall_w, zorder=3)        # hot_wall (left)
    ax.plot([L, L], [0, L], color=COLD, linewidth=wall_w, zorder=3)       # cold_wall (right)
    ax.plot([0, L], [L, L], color=FG_DIM, linewidth=wall_w, zorder=3, linestyle="--")  # adiabatic top
    ax.plot([0, L], [0, 0], color=FG_DIM, linewidth=wall_w, zorder=3, linestyle="--")  # adiabatic bottom

    # Labels
    ax.annotate("hot_wall\nT=305 K", xy=(0, L/2), xytext=(-0.32, L/2),
                color=HOT, fontsize=10, ha="right", va="center",
                arrowprops=dict(arrowstyle="-", color=HOT, lw=1))
    ax.annotate("cold_wall\nT=295 K", xy=(L, L/2), xytext=(L+0.32, L/2),
                color=COLD, fontsize=10, ha="left", va="center",
                arrowprops=dict(arrowstyle="-", color=COLD, lw=1))
    ax.text(L/2, L+0.08, "adiabatic_top", ha="center", color=FG_DIM, fontsize=9)
    ax.text(L/2, -0.12, "adiabatic_bottom", ha="center", color=FG_DIM, fontsize=9)
    ax.text(L/2, L/2, "Boussinesq\nair fluid\nβ=1/T̄\nPr=0.71\nRa=10¹⁰",
            ha="center", va="center", color=FG, fontsize=9.5,
            bbox=dict(boxstyle="round,pad=0.5", facecolor=CARD_BG, edgecolor=FG_MUTE))

    # Front/back patches (empty — 2D-in-3D solver quirk)
    ax.text(L+0.05, 0.02, "front/back: empty\n(2D slice in 3D mesh)",
            fontsize=7.5, color=FG_MUTE, va="bottom", ha="left")

    ax.set_xlim(-0.45, L+0.5)
    ax.set_ylim(-0.2, L+0.2)
    ax.set_aspect("equal")
    ax.set_xlabel("x (m)")
    ax.set_ylabel("y (m)")
    ax.set_title("DHC geometry · square cavity L=1.0 m · generated by\n"
                 "src/foam_agent_adapter.py::_generate_natural_convection_cavity",
                 fontsize=11)
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)

    fig.tight_layout()
    fig.savefig(HERE / "01_geometry.svg", bbox_inches="tight")
    plt.close(fig)
    print("OK 01_geometry.svg")


