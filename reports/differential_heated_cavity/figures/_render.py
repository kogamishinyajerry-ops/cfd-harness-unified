# 2. Mesh topology — 256x256 wall-graded schematic
# =========================================================================
def render_mesh():
    """Downsample 256² to 32² for visual clarity; annotate real params."""
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(11, 5.2), dpi=120,
                                    gridspec_kw={"width_ratios": [1, 0.9]})
    L = 1.0
    n_display = 32
    grading = 6.0  # simpleGrading wall-packed

    # Build wall-graded spacing: double-sided grading with ratio 6
    # The actual blockMeshDict uses simpleGrading ((0.5 0.5 grading)(0.5 0.5 1/grading))
    # For display we use the simpler symmetric grading
    def graded_positions(n, L, r):
        half = n // 2
        # first half: wall-packed from 0 to L/2
        # geometric spacing with expansion ratio = r
        if r == 1.0:
            pos1 = np.linspace(0, L/2, half+1)
        else:
            q = r ** (1/(half-1))
            dx = np.zeros(half)
            dx[0] = (L/2) * (q - 1) / (q**half - 1)
            for i in range(1, half):
                dx[i] = dx[i-1] * q
            pos1 = np.concatenate(([0], np.cumsum(dx)))
        # Mirror for second half (cold wall packed)
        pos2 = L - pos1[::-1][1:]
        return np.concatenate((pos1, pos2))

    xs = graded_positions(n_display, L, grading)
    ys = graded_positions(n_display, L, grading)

    # Left: 2D grid view
    ax1.set_facecolor(DARK_BG)
    for x in xs:
        ax1.plot([x, x], [0, L], color=FG_MUTE, linewidth=0.4, alpha=0.75)
    for y in ys:
        ax1.plot([0, L], [y, y], color=FG_MUTE, linewidth=0.4, alpha=0.75)
    # Emphasize walls
    ax1.plot([0, 0], [0, L], color=HOT, linewidth=2.5)
    ax1.plot([L, L], [0, L], color=COLD, linewidth=2.5)
    ax1.plot([0, L], [L, L], color=FG_DIM, linewidth=2.0, linestyle="--")
    ax1.plot([0, L], [0, 0], color=FG_DIM, linewidth=2.0, linestyle="--")
    ax1.set_xlim(-0.02, 1.02)
    ax1.set_ylim(-0.02, 1.02)
    ax1.set_aspect("equal")
    ax1.set_title(f"Mesh topology schematic (downsampled to {n_display}²)\n"
                  f"actual: 256×256×1 · simpleGrading ratio=6", fontsize=10)
    ax1.set_xlabel("x (m)")
    ax1.set_ylabel("y (m)")

    # Right: first-cell size near wall (the physics that matters)
    ax2.set_facecolor(DARK_BG)
    # Compute actual first-cell for different n (80 vs 256) with grading 6
    def first_cell(n_total, L, r):
        half = n_total // 2
        if r == 1.0:
            return (L/2) / half
        q = r ** (1/(half-1))
        dx0 = (L/2) * (q - 1) / (q**half - 1)
        return dx0

    configurations = [
        ("pre-EX-1-007\n40-cell uniform", 40, 1.0, FAIL),
        ("EX-1-007 #2\n80-cell uniform", 80, 1.0, WARN),
        ("EX-1-007 B1\n256-cell graded=6", 256, 6.0, OK),
        ("fully-resolved target\n~1000-cell graded", 1000, 10.0, ACCENT),
    ]
    labels = [c[0] for c in configurations]
    first_cells = [first_cell(c[1], L, c[2]) * 1000 for c in configurations]  # mm
    colors = [c[3] for c in configurations]

    y_pos = np.arange(len(labels))
    bars = ax2.barh(y_pos, first_cells, color=colors, edgecolor=FG_MUTE, linewidth=0.5)
    ax2.set_yticks(y_pos)
    ax2.set_yticklabels(labels, fontsize=9)
    ax2.set_xlabel("First-cell size near hot wall (mm)", fontsize=10)
    ax2.set_xscale("log")
    ax2.set_title("Wall-normal resolution across configurations", fontsize=10)
    ax2.invert_yaxis()
    ax2.grid(axis="x", alpha=0.2, linestyle="--", color=FG_MUTE)
    ax2.spines["top"].set_visible(False)
    ax2.spines["right"].set_visible(False)

    # Annotate BL thickness reference line
    bl_thickness = 1.0 / 30 * 1000  # δ ≈ L/Nu ≈ 33 mm at gold Nu=30
    ax2.axvline(bl_thickness, color=FUSE, linestyle=":", linewidth=1.5,
                label=f"δ_BL ≈ L/Nu = {bl_thickness:.0f} mm (gold ref)")
    for bar, v in zip(bars, first_cells):
        ax2.text(v*1.15, bar.get_y() + bar.get_height()/2,
                 f"{v:.2f} mm", va="center", fontsize=8.5, color=FG)
    ax2.legend(loc="lower right", fontsize=8)

    fig.tight_layout()
    fig.savefig(HERE / "02_mesh.svg", bbox_inches="tight")
    plt.close(fig)
    print("OK 02_mesh.svg")


